/**
 * 发放工资条 Mock 函数
 */
export function sendPayslipMock(data: {
  salaryMonth: string
  sendMethod: string[]
  sendScope: string
  departmentIds?: number[]
  employeeIds?: number[]
}) {
  const { salaryMonth, sendScope, departmentIds, employeeIds } = data

  // 根据发放范围筛选记录
  let recordsToSend = salaryRecords.filter(item => item.salaryMonth === salaryMonth)

  if (sendScope === 'department' && departmentIds && departmentIds.length > 0) {
    // 这里简化处理，实际应该根据部门ID筛选
    recordsToSend = recordsToSend.filter(item => item.departmentName === '技术部')
  } else if (sendScope === 'employee' && employeeIds && employeeIds.length > 0) {
    recordsToSend = recordsToSend.filter(item => employeeIds.includes(item.employeeId))
  }

  // 更新发放状态
  const now = new Date().toLocaleString('zh-CN', {
    year: 'numeric',
    month: '2-digit',
    day: '2-digit',
    hour: '2-digit',
    minute: '2-digit',
    second: '2-digit',
    hour12: false
  }).replace(/\//g, '-')

  recordsToSend.forEach(record => {
    record.payslipStatus = 2
    record.sendTime = now
  })

  return {
    count: recordsToSend.length
  }
}

